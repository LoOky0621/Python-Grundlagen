#Definition der Funktion

#Definition
def quadrat(x):
    q = x*x
    print("Zahl:", x, "Quadrat:", q)

#Hauptprogramm    
quadrat(4.5)            #Die Variabel in der Definition quadrat wird auf 4.5 gesetzt
a = 3                   #Neue Variabel mit Wert 3
quadrat(a)              #Die Variabel in der Definition quadrat wird auf 3 gesetzt
quadrat(2*a)            #Die Variabel in der Definition quadrat wird auf 6