#If-Else Schleifen
#> größer als
#< kleiner als
#>= größer als oder gleich
#<= kleiner als oder gleich
#== gleich
#!= ungleich

x = 12          
print("x:", x)

if x > 0:                                           #Abfrage ob die Variabel größer als 0 ist
    print("Diese Zahl ist positiv")
else:                                               #Falls die Variabel  kleiner als 0 ist
    print("Diese Zahl ist 0 oder negativ")    
