# Kopfrechnen Spiel Teil3

#Eingabe einer Zahl

print("Bitte eine ganze Zahl eingeben")     #Aufforderung zur Eingabe

x = input()                                 #Eingabe wird die Variabel X zugewissen

print("Ihre Eingabe:", x)                   #Eingabe wird angezeigt

#Umwandlung in eine ganze Zahl

xganz = int(x)                              #Eingabe wird in eine ganze Zahl umgewandelt

print("Als ganze Zahl:", xganz)             #Umgewandelte Zahl wird ausgegeben


#Mit Berechnung

xdoppel = xganz * 2                         #Umgewandelte Zahl wird verdoppelt

print("Das Doppelte:", xdoppel)             #Ausgabe der verdoppelten Zahl